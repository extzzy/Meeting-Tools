function resolveLanguage(setting = "auto") {
  if (setting === "ru" || setting === "en") return setting;
  let locale = "";
  try {
    locale = moment?.locale?.() ?? "";
  } catch {
    locale = "";
  }
  if (!locale && typeof document !== "undefined") locale = document.documentElement?.lang ?? "";
  if (!locale && typeof navigator !== "undefined") locale = navigator.language ?? "";
  return String(locale).toLowerCase().startsWith("ru") ? "ru" : "en";
}

function translate(language, key, variables = {}) {
  const dictionary = messages[language] ?? messages.en;
  const template = dictionary[key] ?? messages.en[key] ?? messages.ru[key] ?? key;
  return String(template).replace(/\{(\w+)\}/g, (_match, name) => variables[name] ?? `{${name}}`);
}

module.exports = { resolveLanguage, translate };
